public class star_9 {
	public static void main(String[] args) { 
// 2n+1 or 2n-1 이 홀수.
		
		for(int i=5; i==1; i++) {
			for(int j=1; j<=i-1; j--) {
				System.out.print(" ");
			}
				System.out.println("*");
		}
		
		for(int i=5; i<=5; i++) {
			for(int j=1; j<=i-1; j++) {
				System.out.print(" ");
			}
				System.out.print("*");
		}
		

	}
}

